def seed_admin_complexes(db):
    from services.verification import sync_complex_verification

    for entry in ADMIN_COMPLEXES:
        data = dict(entry)
        legal_file = data.pop("legal_file")
        existing = db.query(Complex).filter(Complex.slug == data["slug"]).first()
        if existing:
            for key, value in data.items():
                setattr(existing, key, value)
            c = existing
        else:
            c = Complex(**data)
            db.add(c)
            db.flush()

        legal_data = build_admin_legal_report({**entry, "legal_file": legal_file})
        report = db.query(LegalReport).filter(LegalReport.complex_id == c.id).first()
        if report:
            for key, value in legal_data.items():
                setattr(report, key, value)
        else:
            db.add(LegalReport(complex_id=c.id, **legal_data))

        for spec in build_admin_complex_documents({**entry, "legal_file": legal_file}):
            matches = db.query(Document).filter(
                Document.complex_id == c.id,
                Document.doc_type == spec["doc_type"],
            ).order_by(Document.id).all()
            doc = matches[0] if matches else None
            for extra in matches[1:]:
                db.delete(extra)
            if doc:
                for key, value in spec.items():
                    setattr(doc, key, value)
            else:
                db.add(Document(complex_id=c.id, **spec))

        sync_complex_verification(c.id, db)


def remove_duplicate_complexes(db):
    canonical_by_name = {c["name"]: c["slug"] for c in FEATURED_COMPLEXES}
    for entry in ADMIN_COMPLEXES:
        canonical_by_name[entry["name"]] = entry["slug"]

    for slug in LEGACY_DUPLICATE_SLUGS:
        legacy = db.query(Complex).filter(Complex.slug == slug).first()
        if legacy:
            db.delete(legacy)

    for name, canon_slug in canonical_by_name.items():
        for duplicate in db.query(Complex).filter(Complex.name == name, Complex.slug != canon_slug).all():
            db.delete(duplicate)


def seed_database(db):
    admin = db.query(User).filter(User.email == ADMIN_EMAIL).first()
    if not admin:
        admin = User(
            email=ADMIN_EMAIL,
            password_hash=hash_password(ADMIN_PASSWORD),
            full_name="Администратор",
            is_admin=True,
        )
        db.add(admin)
    else:
        admin.password_hash = hash_password(ADMIN_PASSWORD)
        admin.is_admin = True

    seed_featured_complexes(db)
    seed_salkyn_documents(db)
    seed_brown_documents(db)
    seed_eliseiskie_polya_documents(db)
    seed_one_ordo_resort_documents(db)
    seed_siren_documents(db)
    seed_tokio_documents(db)
    seed_green_line_documents(db)
    seed_admin_complexes(db)
    remove_duplicate_complexes(db)
    db.commit()
